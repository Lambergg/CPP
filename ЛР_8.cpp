#include <iostream>
#include <algorithm> // Для sort
#include <vector>

using namespace std;

int main() {

    setlocale(LC_ALL, "Russian");

    const int N = 10; // Размер массива
    int X[N] = { 3, -1, 0, 2, -5, 0, 7, -3, 1, 4 }; // Пример массива

    // Упорядочиваем массив по убыванию
    sort(X, X + N, greater<int>()); // Используем greater для сортировки по убыванию

    // Печатаем упорядоченный массив
    cout << "Упорядоченный массив по убыванию: ";
    for (int i = 0; i < N; ++i) {
        cout << X[i] << " ";
    }
    cout << endl;

    // Инициализация счетчиков
    int k1 = 0; // Количество положительных
    int k2 = 0; // Количество отрицательных
    int k3 = 0; // Количество нулевых

    // Подсчитываем количество положительных, отрицательных и нулевых элементов
    for (int i = 0; i < N; ++i) {
        if (X[i] > 0) {
            ++k1; // Положительный
        }
        else if (X[i] < 0) {
            ++k2; // Отрицательный
        }
        else {
            ++k3; // Нулевой
        }
    }

    // Вывод результатов
    cout << "Количество положительных элементов: " << k1 << endl;
    cout << "Количество отрицательных элементов: " << k2 << endl;
    cout << "Количество нулевых элементов: " << k3 << endl;

    return 0;
}
